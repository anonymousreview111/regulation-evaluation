# Generate the Springer Nature (Artificial Intelligence Review) manuscript from the reframed
# shared section files. Reuses sec_*.tex/tab_*.tex; swaps the sideways matrix for the upright
# three-panel version; emits sn-jnl frontmatter + Statements and Declarations.
import re, os, shutil, glob

SRC = 'paper'; DST = 'paper_springer'
os.makedirs(DST, exist_ok=True)
os.makedirs(os.path.join(DST, 'figures'), exist_ok=True)

main = open(os.path.join(SRC, 'main.tex'), encoding='utf-8').read()

# --- abstract ---
abstract = re.search(r'\\begin\{abstract\}(.*?)\\end\{abstract\}', main, re.S).group(1).strip()

# --- body: from Introduction section to the acknowledgment input ---
b0 = main.index('\\section{Introduction}')
b1 = main.index('\\input{acknowledgment.tex}')
body = main[b0:b1]
# Springer is single-column: full-width starred floats -> normal; use the upright matrix; local figure paths.
body = body.replace('\\input{tab_matrix.tex}', '\\input{tab_matrix_split.tex}')
body = body.replace('{figure*}', '{figure}').replace('{table*}', '{table}')
body = body.replace('../figures/', 'figures/')
body = body.replace('width=0.92\\textwidth', 'width=\\linewidth').replace('width=0.82\\textwidth', 'width=0.85\\linewidth')
body = body.replace('width=\\columnwidth', 'width=\\linewidth')

PRE = r'''%% Artificial Intelligence Review (Springer Nature) -- generated by tools/build_springer.py
%% Build: pdflatex main; bibtex main; pdflatex main; pdflatex main
\documentclass[pdflatex,sn-apacite]{sn-jnl}
\usepackage{graphicx}
\usepackage{amsmath,amssymb,amsfonts}
\usepackage{booktabs}
\usepackage{multirow}
\usepackage{array}
\usepackage{makecell}
\usepackage{wasysym}
\usepackage{textcomp}
\usepackage{apacite}   % provides \citeauthoryear, \APACyear, etc. used by sn-apacite.bst
% Evidence-relevance glyphs (filled / half / open circle = primary / supporting / contextual)
\newcommand{\direct}{\CIRCLE}
\newcommand{\partialev}{\LEFTcircle}
\newcommand{\indirect}{\Circle}
\providecommand{\PARstart}[2]{#1#2}   % sn-jnl has no drop-cap; emit the letters plainly
\raggedbottom

\begin{document}

\title[High-Risk AI Evaluation Mapped to NIST, EU AI Act, ISO/IEC 42001]{Evaluating High-Risk AI for Regulatory Conformity: A Review of Technical Assessment Methods Mapped to the NIST AI RMF, EU AI Act, and ISO/IEC~42001}

%% Named author block withheld for DOUBLE-BLIND review --- uncomment these four lines (and
%% remove the anonymous block below) for the camera-ready version:
%% \author[1]{\fnm{[author withheld]} \sur{[author withheld]}}\email{[email withheld]}
%% \author[2]{\fnm{the second coder} \sur{Singh}}\email{[email withheld]}
%% \affil[1]{\orgname{[affiliation withheld]}, \orgaddress{\city{Miami}, \state{FL}, \postcode{33199}, \country{USA}}}
%% \affil[2]{\orgname{[affiliation withheld]}, \orgaddress{\city{Boston}, \state{MA}, \postcode{02215}, \country{USA}}}
\author[1]{\fnm{} \sur{Anonymous Author(s)}}
\affil[1]{\orgname{Affiliations withheld for double-blind review}}

\abstract{''' + abstract + r'''}

\keywords{AI governance, AI evaluation, conformity assessment, NIST AI RMF, EU AI Act, ISO/IEC 42001}

\maketitle

'''

POST = r'''
\backmatter

\bmhead{Supplementary information}
The verified corpus (with per-record metadata, source status, and family assignment), the
machine-readable crosswalk, the per-database Boolean search strings, and the cell-level
coding/reliability ledger are released as Supplementary Information and in the companion
repository.

\bmhead{Acknowledgements}
%% Camera-ready: ``We thank an external reviewer (ORCID [ORCID withheld]), who is not an author,''
We thank an external reviewer, independent of the author team (name and ORCID withheld for
double-blind review), for independently recoding a stratified sample of the crosswalk---blind
to the first-pass codes---as an external reliability check; the resulting agreement statistics
are reported in Section~\ref{sec:method}.

\section*{Statements and Declarations}
\textbf{Funding.} This work received no specific external funding.\\
\textbf{Competing interests.} The authors declare no competing interests.\\
\textbf{Ethics approval.} Not applicable; this review analyzes only publicly available
documents and the published literature, with no human participants or personal data.\\
\textbf{Data availability.} All analyzed materials are publicly available cited works and the
official NIST, EU, and ISO/IEC texts. The machine-readable crosswalk, the verified corpus, the
per-database Boolean search strings, and the cell-level coding/reliability ledger are
provided as Supplementary Information and, for review, in an anonymized repository
(\url{https://github.com/anonymousreview111}, mirrored at
\url{https://huggingface.co/anonymousreview111}); the permanent public archive with a citable
DOI is provided in the camera-ready version.\\
%% Camera-ready DOI: [DOI withheld for double-blind review] (concept DOI [DOI withheld for double-blind review]).
\textbf{Author contributions.} Author~1 conceived the study, designed the taxonomy and the
methods-to-requirements crosswalk, performed the first-pass coding, and drafted the manuscript.
Author~2 contributed to the methodology and regulatory analysis, independently coded a
stratified sample of the crosswalk cells for the reliability analysis, and critically revised
the manuscript. Both authors approved the final version. (Author names withheld for
double-blind review.)\\
\textbf{AI-use disclosure.} During preparation, the authors used the generative AI assistant
Claude (Anthropic) to help organize the literature, draft and edit prose, assemble and verify
the bibliography, and generate figures and tables. All AI-assisted text was reviewed and edited
by the authors; all regulatory mapping claims and relevance codes were verified independently
against the official sources, and every citation was checked against its primary source. The
authors take full responsibility for the content.

\bibliographystyle{sn-apacite}
\bibliography{references}

\end{document}
'''

open(os.path.join(DST, 'main.tex'), 'w', encoding='utf-8').write(PRE + body + POST)

# copy inputs the manuscript needs
for f in glob.glob(os.path.join(SRC, 'sec_*.tex')) + glob.glob(os.path.join(SRC, 'tab_*.tex')):
    shutil.copy(f, DST)
# sec_mapping.tex inputs tab_matrix.tex; in the Springer build make that the upright version.
shutil.copy(os.path.join(SRC, 'tab_matrix_split.tex'), os.path.join(DST, 'tab_matrix.tex'))
shutil.copy('references.bib', DST)
# Springer class/style/bib-style files (canonical copies live in springer_template/) so the
# build is reproducible even if paper_springer/ is wiped and regenerated.
for support in ('sn-jnl.cls', 'sn-apacite.bst', 'cuted.sty', 'midfloat.sty', 'stfloats.sty'):
    src = os.path.join('springer_template', support)
    if os.path.exists(src):
        shutil.copy(src, os.path.join(DST, support))
for fig in glob.glob('figures/*.pdf'):
    shutil.copy(fig, os.path.join(DST, 'figures'))
print('wrote', os.path.join(DST, 'main.tex'), 'and copied sections/tables/figures/bib')
